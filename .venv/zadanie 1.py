# Импортируем библиотеку pandas, которую мы установили
import pandas as pd

# 1. Загрузка данных
# Указываем имя нашего файла CSV.
# Если файл находится в той же папке, что и main.py, достаточно только имени.
file_path = 'train.csv'

# Используем функцию read_csv() из Pandas для чтения файла
# и сохраняем данные в переменную, которую принято называть DataFrame (df)
try:
    df = pd.read_csv(file_path)
    print("✅ Данные 'Титаник' успешно загружены!")
    print("-" * 40)

    # 2. Вывод данных на экран

    # Выводим первые 5 строк датасета, чтобы посмотреть, как выглядят данные
    print("Первые 5 строк датасета (df.head()):")
    print(df.head())
    print("-" * 40)

    # Выводим общую информацию о датасете (количество строк, столбцов, типы данных)
    print("Информация о датасете (df.info()):")
    df.info()
    print("-" * 40)

    # Выводим размер датасета (количество строк и столбцов)
    print(f"Размер датасета (строк, столбцов): {df.shape}")

except FileNotFoundError:
    print(f"❌ Ошибка: Файл '{file_path}' не найден!")
    print("Убедитесь, что 'train.csv' находится в папке проекта PyCharm.")